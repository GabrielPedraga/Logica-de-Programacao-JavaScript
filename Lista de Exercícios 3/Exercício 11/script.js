function áreaTrapézio(){
    var bmaior = parseFloat(prompt("Digite o tamanho da base maior :"));
    var bmenor = parseFloat(prompt("Digite o tamanho da base menor :"));
    var altura = parseFloat(prompt("Digite a altura :"));
    area = (bmaior+bmenor)*altura/2
    alert("A valor da área do trapézio é : "+area)
}